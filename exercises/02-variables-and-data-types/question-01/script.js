// Declarando com var (escopo mais amplo)
var nomeVar = "João";

// Declarando com let (escopo de bloco)
let nomeLet = "Maria";

// Declarando com const (não pode ser alterada)
const nomeConst = "Carlos";

// Exibindo os valores no console
console.log("var:", nomeVar);
console.log("let:", nomeLet);
console.log("const:", nomeConst);