// Variável com valor inicial
let contador = 5;
console.log("Valor inicial:", contador);

// Incremento com ++
contador++;
console.log("Após contador++:", contador);

// Decremento com --
contador--;
console.log("Após contador--:", contador);

// Atribuição com +=
contador += 3; // contador = contador + 3
console.log("Após contador += 3:", contador);

// Atribuição com -=
contador -= 2; // contador = contador - 2
console.log("Após contador -= 2:", contador);