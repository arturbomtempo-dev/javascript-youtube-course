let day = "terça";

switch (day) {
    case "segunda":
        console.log("Hoje é segunda-feira.");
        break;
    case "terça":
        console.log("Hoje é terça-feira.");
        break;
    case "quarta":
        console.log("Hoje é quarta-feira.");
        break;
    default:
        console.log("Dia não reconhecido.");
}